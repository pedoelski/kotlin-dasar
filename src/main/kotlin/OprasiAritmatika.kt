fun main() {

    val result1:Int = 2 + 2
    println(result1)

    var result2 = 20 + 2 / 2.0
    println(result2)

    //Augmented Assignments
    var total:Int = 0
    var barang1 = 100
    total+=barang1
    var barang2 = 200
    total+=barang2
    var barang3 = 300
    total+=barang3


    //Unary Oprator
    total++
    total++
    total++
    total--

    println(total)

    var sehat = true
    println(!sehat)

}